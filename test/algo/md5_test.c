#include "algo/ct_md5.h"
#include "cunit.h"

static const struct ct_md5_test {
	const char*   data;
	const uint8_t target[16];
} ct_md5_test_all[] = {
	{
		.data   = "hello world",
		.target = {0x5E, 0xB6, 0x3B, 0xBB, 0xE0, 0x1E, 0xEE, 0xD0, 0x93, 0xCB, 0x22, 0xBB, 0x8F, 0x5A, 0xCD, 0xC3},
	},
	{
		.data   = "AAAAA BBBBB CCCCC DDDDD EEEEE",
		.target = {0xC9, 0x0A, 0xA6, 0x25, 0x34, 0x2F, 0x00, 0x8B, 0xD7, 0xCB, 0x12, 0x04, 0x10, 0x98, 0x33, 0xA5},
	},
	{
		.data   = "ABCDE abcde 12345 ABCDE abcde 12345",
		.target = {0xC1, 0x60, 0xFA, 0x12, 0xF4, 0x4B, 0x0E, 0x95, 0x34, 0xEC, 0xB9, 0x7E, 0x78, 0x5E, 0x30, 0x5B},
	},
	{
		.data   = " ABCDE abcde 12345 ABCDE abcde 12345",
		.target = {0x3D, 0xA5, 0x87, 0x18, 0xCD, 0x46, 0x88, 0xE7, 0xA4, 0x95, 0x58, 0xC3, 0x45, 0xFF, 0x12, 0xE9},
	},
	{
		.data   = "ABCDE abcde 12345 ABCDE abcde 12345 ",
		.target = {0x67, 0x84, 0x11, 0xBD, 0xE3, 0x5B, 0xCC, 0xC8, 0x09, 0xFB, 0x91, 0xBA, 0xF2, 0xF3, 0x37, 0x8F},
	},
	{
		.data   = " ABCDE abcde 12345 ABCDE abcde 12345 ",
		.target = {0x0A, 0xE2, 0xE0, 0xC3, 0x11, 0x74, 0x3E, 0xB9, 0x6E, 0xFA, 0x7F, 0x59, 0x00, 0xAB, 0x90, 0x49},
	},
};

static inline int test_cmd5(void) {
	uint8_t buffer[16];

	const int size = (int)ct_arrsize(ct_md5_test_all);
	for (int i = 0; i < size; i++) {
		const struct ct_md5_test* it = &ct_md5_test_all[i];

		ct_md5_ctx_t ctx;
		ct_md5_init(&ctx);
		ct_md5_update(&ctx, it->data, strlen(it->data));
		ct_md5_final(&ctx, buffer);
		cunit_assert_string_hex(buffer, it->target, 16, "i=%d", i);
	}

	return 0;
}

int main(void) {
	test_cmd5();
	cunit_println("Finish! test_cmd5();\n");

	cunit_pass();
}
